# Given string
text = 'My name is LEE TAE GYU'

# 1. 문자열의 문자수를 출력
print("문자수:", len(text))

# 2. 문자열의 10번 반복한 문자열을 출력
print("10번 반복:", text * 10)

# 3. 문자열의 첫 번째 문자를 출력하라.
print("첫 번째 문자:", text[0])

# 4. 문자열에서 처음 4문자를 출력하라.
print("처음 4문자:", text[:4])

# 5. 문자열에서 마지막 4문자를 출력하라.
print("마지막 4문자:", text[-4:])

# 6. 문자열의 문자를 거꾸로 출력하라.
print("거꾸로 출력:", text[::-1])

# 7. 문자열에서 첫 번째 문자와 마지막 문자를 제거한 문자열을 출력하라.
print("첫 문자와 마지막 문자 제거:", text[1:-1])

# 8. 문자를 모두 대문자로 변경하여 출력하라.
print("대문자로:", text.upper())

# 9. 문자를 모두 소문자로 변경하여 출력하라.
print("소문자로:", text.lower())

# 10. 문자열에서 'a'를 'e'로 대체하여 출력하라.
print("'a'를 'e'로 대체:", text.replace('a', 'e'))