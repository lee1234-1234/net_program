# Define the string
text = "Hello, IoT"

# A. Print the number of characters in the string
print("A. Number of characters:", len(text))

# B. Print the string repeated 5 times
print("B. String repeated 5 times:", text * 5)

# C. Print the first 3 characters of the string
print("C. First 3 characters:", text[:3])

# D. Print the last 3 characters of the string
print("D. Last 3 characters:", text[-3:])

# E. Convert the string to uppercase and print
print("E. String in uppercase:", text.upper())